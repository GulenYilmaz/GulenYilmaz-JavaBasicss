package syntax.class01__Intro_And_DataTypes;

public class PrimitiveDataTypes {
	
	public static void main(String[] args) {
	
	// 8 primitive datatypes
		//to store whole numbers--> byte, short, int, long
		
		byte variableOne=10;
		//up to 127, if you use 128 it looks error		
		short variable2=100;
		//up to  32767
		int variableThree=1000;
		//up to ...
		
		long variable_four=100000l;
		//l or L we can add at the end of the number
		
		// to store decimal numbers--> float, double
		
		float valiable$five=12.99f;
		
		// we put f after value  at float
		
		double anyName=132.89;
		
		
		// to store single character--> char
		
		char singleValue='F';
				
		char anotherVariable='%';
		
		//to store boolean values--> boolean
		
		boolean hungry=true;
		boolean thirsty=false;
		
		System.out.println(hungry);
		
		
		
	
	}
}
